# Data Types in Python

# Integer value
a = -356
# Float Value - decimal value
b = 67.8 
# String  - set of characters
c = "Aditi is doing well"
d = 'Aditi'
e = '''Aditi'''
# Boolean values - true or false
f = False
# None value
g = None

print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)

print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))